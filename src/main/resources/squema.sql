
DROP TABLE IF EXISTS USER;
DROP TABLE IF EXISTS GRUPO; 

DROP TABLE IF EXISTS PERIODO; 

DROP TABLE IF EXISTS GASTO; 

CREATE TABLE USER (
  USER_ID int(11) NOT NULL AUTO_INCREMENT,
  USERNAME varchar(255) DEFAULT NULL,
  FIRST_NAME varchar(50) DEFAULT NULL,
  LAST_NAME varchar(50) DEFAULT NULL,
  GENDER varchar(10) DEFAULT NULL,
  CONTRASENA varchar(255) DEFAULT NULL,
  ESTADO tinyint(10) DEFAULT NULL,
  PRIMARY KEY (USER_ID)
);



CREATE TABLE PERIODO(
  PERIODO_CODE int(11) NOT NULL AUTO_INCREMENT,
  USER_ID int(11) NOT NULL,
  NOMBRE_PERIODO VARCHAR2(40) NOT NULL,
  MES VARCHAR2(40) NOT NULL,
  PRIMARY KEY (PERIODO_CODE)
); 




CREATE TABLE GRUPO(
  GRUPO_CODE int(11) NOT NULL AUTO_INCREMENT,
  PERIODO_CODE int(11) NOT NULL,
  NOMBRE_GRUPO VARCHAR2(40) NOT NULL,
  FOTO VARCHAR2(255),
  PRIMARY KEY (GRUPO_CODE)
); 


CREATE TABLE GASTO(
  ID_GASTO  int(11) NOT NULL AUTO_INCREMENT,
  GRUPO_CODE int(11) NOT NULL,
  CONCEPTO VARCHAR2(40) NOT NULL,
  IMPORTE FLOAT NOT NULL,
  FECHA DATE NOT NULL,
  PRIMARY KEY(ID_GASTO)
); 


